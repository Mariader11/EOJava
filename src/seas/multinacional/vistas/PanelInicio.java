    private void setPrecioTotal() {
        int filas = modeloTbl.getRowCount();
        float precioTotal = 0;
        for (int i = 0; i < filas; i++)
            precioTotal = precioTotal +  (Float)modeloTbl.getValueAt(i, 4);       
        txtTotal.setText(Float.toString(precioTotal));
    }
