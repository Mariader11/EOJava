    private void btnAmericanoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAmericanoActionPerformed
        frame.setProducto(frame.getProductoById(1));
        modeloTbl.addRow(new Object[]{frame.getCantidad(), frame.getProducto().getNombre(), frame.getProducto().getPrecio(), 0, frame.getCantidad()*frame.getProducto().getPrecio()}); 
        tblCompra.setRowSelectionInterval(modeloTbl.getRowCount()-1, modeloTbl.getRowCount()-1); //selecciona la última fila de la tabla
        idProductos.add(1);
        frame.setCantidad(0);
        setPrecioTotal();
        try {
            recopilarDatos();
        } catch (ParseException ex) {
            Logger.getLogger(PanelInicio.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAmericanoActionPerformed

