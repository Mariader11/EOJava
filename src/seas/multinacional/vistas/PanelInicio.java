    /**
     * Botón que muestra los clientes registrados y rellena los cuadros de texto con el seleccionado
     * @param evt 
     */
    private void btnClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClienteActionPerformed
      String inputValue = (String) JOptionPane.showInputDialog(getFrame(), "Seleccione el cliente:", "Clientes", JOptionPane.DEFAULT_OPTION,  new javax.swing.ImageIcon(getClass().getResource("/seas/multinacional/iconos/cliente.png")), acciones.getClientes(), acciones.getClientes()[0] );
      if (inputValue != null) {
        String[] texto = acciones.setClientInTextBoxes(inputValue);
        txtCodigoCliente.setText(texto[0]); //id
        txtNombre.setText(texto[1]); //nombre y apellidos
        frame.setCliente(acciones.getClienteById(Integer.parseInt(texto[0])));
      }
    }//GEN-LAST:event_btnClienteActionPerformed

