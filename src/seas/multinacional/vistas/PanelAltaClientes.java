    private void btnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarMouseClicked
        if(!getTxtNombre().getText().isEmpty() && !getTxtApellido1().getText().isEmpty()){
            String nombre = getTxtNombre().getText().trim(); 
            String apellido1 = getTxtApellido1().getText().trim();
            String apellido2 = getTxtApellido2().getText().trim();
            String clave = getTxtClave().getText().trim();     
            acciones.saveCliente(nombre, apellido1, apellido2, clave, 0);
            JOptionPane.showMessageDialog(null, "Datos ingresados correctamente.");  
            reset();
        }   else {
            JOptionPane.showMessageDialog(null, "No puedes dejar vac√≠o el campo de nombre o el primer apellido");
        }
    }//GEN-LAST:event_btnGuardarMouseClicked

